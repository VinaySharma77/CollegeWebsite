            <h2>Principal's Message</h2>
            <div class="principal-box">
                <div class="principal-message">
                    <p>Post Graduate Govt. College Sector 11, now a Co-educational Institute, was established in 1953. Over the years, our illustrious students have carved a niche for themselves in fields as diverse as Politics, Administration, Business, Law etc. We offer a broad curriculum and a range of opportunities for all our students to achieve excellence in academic, creative, social, cultural, sporting and community endeavours.
                        There are as many as 30 departments in Humanities, Commerce, Science, Information Technology, Physical Education as well as PG courses in Chemistry, Commerce, History, Hindi, English, Physics, Punjabi, Physical Education and Information Technology.
                        The college boasts of a large sports ground with cinder track and a sports pavilion. There is also a swimming pool, a gymnasium, tennis, golf course and a basketball court. It has a department of Environment Education with a highly specialized Cosmic Garden, the first of its kind in India with special reference to Phytochemicals as an alternative medicine.
                        Our college is spread over an area of 43.8 acres and is equipped with the latest technology, numerous internet labs, Wi-Fi facility, overall e-surveillance and the largest library among the college in this city. The library is fully computerized with online access and a new feature of transaction messaging through SMS. It has made old question papers and data of pending degrees since 1958 available online. It has a separate Braille corner for visually challenged students.
                        Students, we believe are our most valued assets. A dedicated team of our teachers monitor and mentor young students to help them imbibe attributes, skills and capabilities to achieve their cherished goals. I am positive that in times to come, we will make steady progress in this rewarding journey of teaching and learning in Post Graduate Govt. College Sector 11, Chandigarh.</p>
